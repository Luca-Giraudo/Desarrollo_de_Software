"""Promedio de temperaturas"""
temp1 = int(input("Ingresar temperatura del dia 1: "))
temp2 = int(input("Ingresar temperatura del dia 2: "))
temp3 = int(input("Ingresar temperatura del dia 3: "))
temp4 = int(input("Ingresar temperatura del dia 4: "))
temp5 = int(input("Ingresar temperatura del dia 5: "))
promedio = (temp1+temp2+temp3+temp4+temp5) / 5
print(f"La temperatura promedio de los 5 dias es {promedio}")